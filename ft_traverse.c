#include "push_swap.h"

void	ft_traverse(t_stack *st, int *arr)
{
	int	i;

	i = 0;
	while (i < st->size)
	{
		ft_printf("%d ", arr[i]);
		++i;
	}
	ft_printf("\n");
}
