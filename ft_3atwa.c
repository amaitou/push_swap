#include "push_swap.h"

long	ft_3atwa(char *p)
{
	int		s;
	long	r;

	s = 1;
	r = 0;
	while (*p == ' ' || *p == '\n' || *p == '\t' || *p == '\v' || *p == '\f'
		|| *p == '\r')
		++p;
	if (*p == '-' || *p == '+')
	{
		if (*p == '-' && *(p + 1) == '0' && !*(p + 2))
		{
			ft_putendl_fd("Error", 2);
			exit(0);
		}
		if (*p == '-')
			s *= -1;
		++p;
	}
	while (*p >= '0' && *p <= '9')
	{
		r = r * 10 + *p - 48;
		++p;
	}
	if (r >= INT_MAX || r <= INT_MIN)
	{
		ft_putendl_fd("Error", 2);
		exit(0);
	}
	return (r * s);
}
